    private Long companyId;
    @XSSFreeString
    private String name;

    @XSSFreeString
    private String address;

    @XSSFreeString
    private String city;

    @XSSFreeString
    private String state;

    @XSSFreeString
    private String postalCode;

    @XSSFreeString
    private String country;

    @XSSFreeString
    private String phone;

